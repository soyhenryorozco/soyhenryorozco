function formatViews(count) {
  return new Intl.NumberFormat('es-CO').format(count) + ' vistas';
}

function createSpotifyEmbed(type, id) {
  const iframe = document.createElement('iframe');
  iframe.src = `https://open.spotify.com/embed/${type}/${id}?utm_source=generator&theme=0`;
  iframe.style.border = '0';
  iframe.style.borderRadius = '12px';
  iframe.style.width = '100%';
  iframe.style.height = '100%';
  iframe.style.display = 'block';
  iframe.setAttribute('allowfullscreen', '');
  iframe.allow = 'autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture';
  iframe.loading = 'lazy';
  iframe.title = type === 'artist' ? 'Henry Orozco en Spotify' : 'Quiero ser — Henry Orozco';
  return iframe;
}

